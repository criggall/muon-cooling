BLCMDrdecaysource::BLCMDrdecaysource() : BLCommand(), BLSourceRun(),
					isotopeData(), isotopeNames(), out(0),
					particleGun(0), generating(false),
					lastTrackID(-99), sourceIon(),
					lostKE(0.0), decayTime(0.0), 
					weight(1.0), eventID(0), startTimems(0)
{
	registerCommand(BLCMDTYPE_BEAM);
	setSynopsis("Generates a 'beam' from radioactive isotopes in the world.");
	setDescription(
	 "Scans the world for radioactive isotopes. If the isotopes argument "
	 "is set, only those are used, otherwise all unstable isotopes are "
	 "used. Unstable isotopes are decayed in place, wherever they "
	 "appear in the geometry.\n\n"
	 "The argument 'duration' specifies over what time interval (from t=0) "
	 "generation will take place. The weighted sum of all generated "
	 "tracks will be appropriate for the radioactive decay of the "
	 "unstable nuclei in the world over that duration.\n\n"
	 "This source has two modes: mode=1 is appropriate for use by "
	 "a simulation program that handles radioactive decay itself; mode=2 "
	 "is appropriate for a simulation program that does not. mode=1 "
	 "preserves the time dependence of ion energy deposition, mode=2 does "
	 "not; both preserve the overall ion energy deposition (over all "
	 "time).\n\n"
	 "In mode 1 (default): "
	 "this source generates all ordinary particles that are produced "
	 "within duration of t=0. Unstable nuclei with lifetimes < 1us "
	 "have their kinetic energy saved, set to 0, and then are tracked so "
	 "they decay. Stable nuclei, and unstable nuclei with lifetime >= "
	 "1us are output and killed, adding any saved KE. This makes the "
	 "approximation that no unstable nucleus moves significantly, but "
	 "the energy deposited by ions is preserved, including its time "
	 "dependence (within 1us).\n\n"
	 "In mode 2: "
	 "this source generates all ordinary particles and stable ions that "
	 "result from the initial nuclear decay. Unstable nuclei in the decay "
	 "chain are not output, but are tracked so they decay. This makes the "
	 "approximation that no unstable nucleus moves significantly; its "
	 "kinetic energy is forced to 0, and its original kinetic energy "
	 "is added to the next stable ion that is output -- this preserves "
	 "the energy deposited by ions (but not its time dependence), and "
	 "avoids generating extraneous very-low-energy electrons.\n\n"
	 "This source generates the appropriate weighted number of events for "
	 "duration; if nEvents is 0, the appropriate number of events is "
	 "generated so they have weight=1; if nEvents "
	 "is set, that many events will be generated with weight set "
	 "appropriately; duration and nEvents apply to the first radioactive "
	 "decay, but not to subsequent decays in the chain. Note that to get "
	 "the entire chain in mode=2, 'trackcuts maxtime=1E99' is normally "
	 "needed, as radioactive decay is quite slow (can be gigayears).\n\n"
	 "File format is always BLTrackFile (i.e ASCII).\n\n"
	 "This command is not placed into the geometry.\n\n"
	 "verbose controls printout vebosity:\n"
	 " 0 print only event summary.\n"
	 " 1 also print summary of unstable nuclei in volumes (default).\n"
	 " 2 also print summary of tracks generated (one line per track).\n"
	 " 3 also print steppingVerbose.\n"
	 " 4 also print details of stacking action.\n"
	 " 5 track all particles regardless of mode (DEBUGGING - double counts).\n"
	);
	isotopes = "";
	duration = 0.0;
	nEvents = 0;
	first = 1;
	maxTime = 1E99;
	mode = 1;
	verbose = 1;
	showMarkers = 0;
	showDecayChain = 0;
	totalEvents = 0L;
	generateEvents = 0L;
	generatedEvents = 0L;
	generatedTracks = 0L;
}

int BLCMDrdecaysource::command(BLArgumentVector& argv, BLArgumentMap& namedArgs)
{
	static bool once=true;
	if(!once) {
		printError("rdecaysource can only be used once.");
		return -1;
	}
	once = false;
	if(BLManager::getObject()->getPhysics() == 0) {
		printError("rdecaysource: no physics; the physics command MUST come first.");
		return -1;
	}

	handleNamedArgs(namedArgs);

	std::vector<G4String> list = splitString(isotopes,",",true);
	for(unsigned i=0; i<list.size(); ++i) {
		if(list[i] == "") continue;
		isotopeNames.insert(list[i]);
	}

	out = new BLTrackFile("rdecaysource.txt","rdecaysource","w",1);
	out->flush();

	// Note this class does NOT inhereit BLBeam -- it is used only in a
	// separate source run. But it adds a beam command to read the file
	// it generates (rdecaysource.txt);
	BLManager::getObject()->registerSourceRun(this);

	// we write rdecaysource.txt, so must have the simulation read it.
	G4String cmd("beam ascii filename=rdecaysource.txt");
	BLCommand::doCommand(cmd);

	// set maxTime
	BLManager::getObject()->getPhysics()->setMaxTime(maxTime);

	print("");

	return 0;
}

void BLCMDrdecaysource::defineNamedArgs()
{
	argString(isotopes,"isotopes","Comma-separated list of isotopes (default=All).");
	argDouble(duration,"duration","Duration of generation (1us).");
	argInt(nEvents,"nEvents","Number of events to generate (default is however many are generated during duration).");
	argInt(mode,"mode","Mode of the source (see text).");
	argInt(first,"first","First EventID to generate (default=1).");
	argDouble(maxTime,"maxTime","Maximum time in lab (1E99 ns).");
	argInt(verbose,"verbose","Verbosity level of printouts (default=1).");
	argInt(showMarkers,"showMarkers","Set nonzero to display a marker at the origin of each generated track.");
	argInt(showDecayChain,"showDecayChain","# events to display the decay chan (0).");
	argInt(dump,"dump","Set nonzero to dump the ion table.");
}

void BLCMDrdecaysource::prepare()
{
	if(dump)
		dumpTable();

	lostKE = 0.0;
	eventID = 0;
	startTimems = BLTime::timems();
	generatedEvents = 0L;
	generatedTracks= 0L;
	markers = new BLMarkers;
	particleGun = new G4ParticleGun(1);

	G4AffineTransform identity;
	scan(BLManager::getObject()->getWorldPhysicalVolume(),identity);

	// determine # events in duration
	if(duration == 0.0) duration = 1.0*microsecond;
	long nTotal = 0;
	for(unsigned i=0; i<isotopeData.size(); ++i) {
		IsotopeData &id = isotopeData[i];
		double lifetime = id.lifetime;
		double nAtoms = id.totalAtoms;
		// big #s - use first-order to avoid loss of significance
		double x = duration/lifetime;
		// but modest #s - compute it correctly
		if(x > 1E-9) x = (1.0 - exp(-duration/lifetime));
		id.nEvents = nAtoms * x;
		nTotal += id.nEvents;
	}

	// determine totalEvents, based on duration and nEvents (arguments)
	totalEvents = nTotal;
	if(totalEvents == 0) {
		printf("\nrdecaysource: no radioactive isotopes\n");
		return;
	}

	// re-scale to nEvents
	if(nEvents > 0) {
		weight = (double)nTotal / (double)nEvents;
		totalEvents = 0;
		for(unsigned i=0; i<isotopeData.size(); ++i) {
			IsotopeData &id = isotopeData[i];
			id.nEvents *= (double)nEvents / (double)nTotal;
			totalEvents += id.nEvents;
		}
		generateEvents = nEvents;
	} else {
		weight = 1.0;
		nEvents = generateEvents = totalEvents;
	}

	if(verbose >= 1) {
		printf("\nrdecaysource IsotopeData:\n");
		for(unsigned i=0; i<isotopeData.size(); ++i) {
	    	    IsotopeData &id = isotopeData[i];
	    	    printf("%s totalAtoms=%.4g lifetime=%.4gsec nEvents=%ld\n",
				id.iso->GetName().c_str(),id.totalAtoms,
				id.lifetime/second,id.nEvents);
		    double mass = id.particleDefinition->GetPDGMass();
	    	    std::vector<VolumeData> &vd = id.volumeData;
	    	    for(unsigned j=0; j<vd.size(); ++j) {
			printf("    physVol=%s nAtoms=%.4g mass=%.4gkg\n",
				vd[j].physVol->GetName().c_str(),vd[j].nAtoms,
				mass*vd[j].nAtoms/c_light/c_light/kg);
	    	    }
		}
		if(duration > 1E9)
		   printf("rdecaysource: totalEvents=%ld duration=%.4gsec weight=%.3g\n\n",
				generateEvents, duration/second, weight);
		else if(duration > 1000)
		   printf("rdecaysource: totalEvents=%ld duration=%.0fns weight=%.3g\n\n",
				generateEvents, duration/ns, weight);
		else
		   printf("rdecaysource: totalEvents=%ld duration=%.3fns weight=%.3g\n\n",
				generateEvents, duration/ns, weight);
	}
}

void BLCMDrdecaysource::shutDown()
{
	long time = BLTime::timems() - startTimems;
	printf("rdecaysource: generated %ld events, %ld tracks, in %.3f seconds\n",
			generatedEvents,generatedTracks,(double)time/1000.0);
}

bool BLCMDrdecaysource::nextSourceEvent(G4Event *event)
{
	if(generateEvents <= 0) {
		if(out) delete out;
		out = 0;
		generating = false;
		return false;
	}
	generating = true;

	// select an isotope
	long v = G4UniformRand() * totalEvents;
	unsigned indx;
	for(indx=0; indx<isotopeData.size(); ++indx) {
		v -= isotopeData[indx].nEvents;
		if(v <= 0) break;
	}
	assert(indx < isotopeData.size());
	IsotopeData &id = isotopeData[indx];

	// select a physical volume
	double vv = G4UniformRand() * id.totalAtoms;
	unsigned indx2;
	for(indx2=0; indx2<id.volumeData.size(); ++indx2) {
		vv -= id.volumeData[indx2].nAtoms;
		if(vv <= 0.0) break;
	}
	assert(indx2 < id.volumeData.size());
	VolumeData &vd = id.volumeData[indx2];

	// generate a point in vd.physVol but outside any daughters
	G4ThreeVector pos;
	G4Navigator *nav = G4TransportationManager::GetTransportationManager()->
						GetNavigatorForTracking();
	for(int i=0; i<1000; ++i) {
		pos = generatePointInSolid(vd.solid);
		/* ? */pos = vd.local2global.TransformPoint(pos);
		G4VPhysicalVolume *pv = nav->LocateGlobalPointAndSetup(pos);
		if(pv == vd.physVol) break;
	}
	// if all 1000 are in a daughter, live with the last one
	//@pos = vd.local2global.TransformPoint(pos);

	// generate other variables
	// NOTE: this is the initial unstable isotope, and we generate it
	// at time=0. In ClassifyNewTrack, its children are forced to
	// have time randomly between 0 and duration.
	double time = 0.0;
	G4ThreeVector momentum;
	int eventId=event->GetEventID(), trackId=1, parentId=0;

	sourceIon = id.iso->GetName().c_str();

	particleGun->SetParticleDefinition(id.particleDefinition);
	particleGun->SetParticleTime(time);
	particleGun->SetParticlePosition(pos);
	particleGun->SetParticleEnergy(0.0);
	particleGun->GeneratePrimaryVertex(event);
	event->GetPrimaryVertex()->SetWeight(weight);

	if(showMarkers != 0) markers->addMarker(pos);
	lastTrackID = trackId;
	--generateEvents;
	++generatedEvents;
	return true;
}

void BLCMDrdecaysource::BeginOfEventAction(const G4Event* event) 
{
	eventID = event->GetEventID() + first - 1;
	if(verbose >= 2)
		printf("\nEvent %d rdecaysource %s\n",eventID,sourceIon.c_str());
	DecayChain::clear();
	decayTime = G4UniformRand() * duration;
}

void BLCMDrdecaysource::EndOfEventAction(const G4Event* event)
{
	if(showDecayChain-- > 0) DecayChain::print();
}

void BLCMDrdecaysource::PreUserTrackingAction(const G4Track *track)
{
	if(verbose >= 3)
		steppingVerbosePrint(0,track,true);
}

void BLCMDrdecaysource::UserSteppingAction(const G4Step *step)
{
	if(verbose >= 3)
		steppingVerbosePrint(step,step->GetTrack());
}

G4ClassificationOfNewTrack BLCMDrdecaysource::ClassifyNewTrack(
							const G4Track *track)
{
	BLAssert(generating);

	// always keep track 1
	if(track->GetTrackID() == 1) {
		if(showDecayChain > 0) {
			DecayChain::add(track->GetParticleDefinition(),
			      track->GetKineticEnergy(),track->GetGlobalTime(),
			      track->GetTrackID(),track->GetParentID());
		}
		lostKE = 0.0;
		return fUrgent;
	}

	// force children of the initial unstable isotope to have the correct
	// time of decay.
	if(track->GetParentID() == 1) {
		const_cast<G4Track*>(track)->SetGlobalTime(decayTime);
		if(verbose >= 4)
		    printf("Classify track %d: set creation time to %.3fns\n",
		    			track->GetTrackID(),decayTime/ns);
	}

	// initial values. Note that setting ret = fKill will also output the 
	// track.
	G4ClassificationOfNewTrack ret = fUrgent;
	const char *type = "";
	char msg[64] = "";

	if(track->GetGlobalTime() > duration+0.1*ns && mode == 1) {
		G4Exception("rdecaysource","Late particle",JustWarning,
				"Particle generated with time > duration");
		ret = fKill;
		type = "Late Particle";
	} else if(track->GetParticleDefinition()->IsGeneralIon()) {
		double lifetime = 
			track->GetParticleDefinition()->GetIonLifeTime();
		//@ workaround for HAD_RDM_011: lifetime = -1001 means
		//@ very short lived and not in the table.
		if(fabs(lifetime - -1001.0) < 0.1) {
static bool once=true;
if(once) G4Exception("rdecaysource","Workaround for HAD_RDM_011",JustWarning,"Will be fixed in Geant4.10.1");
once = false;
			lifetime = DBL_MIN;
			const_cast<G4ParticleDefinition*>(
				track->GetParticleDefinition())->
						SetPDGLifeTime(lifetime);
		}
		if(lifetime >= 0.0) {
		    switch(mode) {
		    case 1:
		    	// unstable ion
			if(lifetime < 1.0*microsecond) { // short-life - track
				goto track_ion;
			} else {	      	// long-life - output&kill
				// add any lostKE
				lostKE += track->GetKineticEnergy();
				const_cast<G4Track*>(track)->
			    			SetKineticEnergy(lostKE);
				lostKE = 0.0;
				ret = fKill;
				type = "long-lifetime Ion";
			}
			break;
		    case 2:
track_ion:		// unstable ion - add its KE to lostKE, set its KE
			// to 0, track it
			if(verbose >= 4)
				snprintf(msg,sizeof(msg),"KE=%.3fMeV",
						track->GetKineticEnergy());
			lostKE += track->GetKineticEnergy();
			const_cast<G4Track*>(track)->SetKineticEnergy(0.0);
			ret = fUrgent;
			type = "Unstable Ion";
		    }
		} else {
			// stable ion -- output and kill
			if(verbose >= 4)
				snprintf(msg,sizeof(msg),
					"KE=%.3fMeV + lostKE=%.3fMeV",
					track->GetKineticEnergy(),lostKE);
			// add any lostKE
			lostKE += track->GetKineticEnergy();
			const_cast<G4Track*>(track)->SetKineticEnergy(lostKE);
			lostKE = 0.0;
			ret = fKill;
			type = "Stable Ion";
		}
	} else {
		// ordinary particle -- output and kill
		ret = fKill;
		type = "Ordinary Particle";
	}

	if(verbose >= 4)
	    printf("Classify track %d: PDGid=%d %s (%s) disposition=%s %s\n",
		    track->GetTrackID(),
		    track->GetParticleDefinition()->GetPDGEncoding(),
		    track->GetParticleDefinition()->GetParticleName().c_str(),
		    type,(ret == fKill ? "output&kill" : "track"),
		    msg);

	if(showDecayChain > 0) {
		DecayChain::add(track->GetParticleDefinition(),
			track->GetKineticEnergy(),track->GetGlobalTime(),
			track->GetTrackID(),track->GetParentID());
	}

	if(ret == fKill) {
		if(out) out->write(track->GetPosition(),track->GetGlobalTime(),
			       track->GetMomentum(),
			       track->GetParticleDefinition()->GetPDGEncoding(),
			       eventID,track->GetTrackID(),track->GetParentID(),
			       weight);
		++generatedTracks;
		if(verbose >= 2) {
			printf("      Track %d %s KE=%.3fMeV t=%.4gns\n",
				track->GetTrackID(),
				track->GetParticleDefinition()->
						GetParticleName().c_str(),
				track->GetKineticEnergy()/MeV,
				track->GetGlobalTime()/ns);
		}
	}

	if(verbose >= 5)
		ret = fUrgent;
	return ret;
}

void BLCMDrdecaysource::scan(const G4VPhysicalVolume *physVol, 
						G4AffineTransform &local2global)
{
	static G4String prefix="";

	const G4LogicalVolume *logVol = physVol->GetLogicalVolume();
	int nDaughters = logVol->GetNoDaughters();

	// compute volume
	G4double volume = logVol->GetSolid()->GetCubicVolume();
	for(int i=0; i<nDaughters; ++i) {
		const G4VPhysicalVolume *d = logVol->GetDaughter(i);
		volume -= d->GetLogicalVolume()->GetSolid()->GetCubicVolume();
	}

	// handle isotopes of material
	const G4Material *mat = logVol->GetMaterial();
	const G4ElementVector *elementVector = mat->GetElementVector();
	const G4double *atomsPerVolume = mat->GetVecNbOfAtomsPerVolume();
	int nElements = elementVector->size();
	for(int i=0; i<nElements; ++i) {
		const G4Element *element = mat->GetElement(i);
		double atoms = atomsPerVolume[i] * volume;
		int nIsotopes = element->GetNumberOfIsotopes();
		const double *relAbund = element->GetRelativeAbundanceVector();
		for(int j=0; j<nIsotopes; ++j) {
			const G4Isotope *isotope = element->GetIsotope(j);
			if(isotopeNames.size() > 0 && 
			   isotopeNames.count(isotope->GetName()) == 0) 
			   	continue;
			// isRadioactive() ensures a ParticleDefinition exists
			if(!isRadioactive(isotope,prefix)) continue;
			double f = relAbund[j];
			handleIsotope(isotope,physVol,atoms*f,local2global);
		}
	}

	// scan daughters
	G4String oldPrefix = prefix;
	prefix += "  ";
	for(int i=0; i<nDaughters; ++i) {
		const G4VPhysicalVolume *d = logVol->GetDaughter(i);
		G4AffineTransform l2g = G4AffineTransform(
				d->GetFrameRotation(),
				d->GetObjectTranslation()) * local2global;
		scan(d,l2g);
	}
	prefix = oldPrefix;
}

void BLCMDrdecaysource::handleIsotope(const G4Isotope *iso,
				const G4VPhysicalVolume *physVol, double nAtoms,
				G4AffineTransform &local2global)
{
	unsigned ind;
	for(ind=0; ind<isotopeData.size(); ++ind) {
		if(isotopeData[ind].iso == iso) break;
	}
	if(ind == isotopeData.size())
		isotopeData.push_back(IsotopeData(iso));

	isotopeData[ind].addVolume(physVol,nAtoms,local2global);
}

BLCMDrdecaysource::VolumeData::VolumeData(const G4VPhysicalVolume *_physVol,
				double _nAtoms, G4AffineTransform &xform) :
				physVol(_physVol), nAtoms(_nAtoms), 
				local2global(xform)
{
	logVol = physVol->GetLogicalVolume();
	solid = logVol->GetSolid();;
}

void BLCMDrdecaysource::IsotopeData::addVolume(const G4VPhysicalVolume *_physVol, 
				double _nAtoms, G4AffineTransform &local2global)
{
	volumeData.push_back(VolumeData(_physVol,_nAtoms,local2global));
	totalAtoms += _nAtoms;
}

bool BLCMDrdecaysource::isRadioactive(const G4Isotope *iso, G4String prefix)
{
	double lifetime = G4IonTable::GetIonTable()
			->GetIon(iso->GetZ(),iso->GetN(),0)->GetIonLifeTime();
	return lifetime > 0.0;
}

G4ThreeVector BLCMDrdecaysource::generatePointInSolid(const G4VSolid *s)
{
	G4VisExtent ext = s->GetExtent();
	double x0 = ext.GetXmin();
	double dx = ext.GetXmax() - x0;
	double y0 = ext.GetYmin();
	double dy = ext.GetYmax() - y0;
	double z0 = ext.GetZmin();
	double dz = ext.GetZmax() - z0;

	for(int i=0; i<100; ++i) {
		G4ThreeVector pos(G4UniformRand()*dx+x0,G4UniformRand()*dy+y0,
							G4UniformRand()*dz+z0);
		if(s->Inside(pos) == kInside) {
			return pos;
		}
	}

	// punt -- just return the center (implies volume occupies < 1% of box)
	return G4ThreeVector(dx/2+x0,dy/2+y0,dz/2+z0);
}

void BLCMDrdecaysource::steppingVerbosePrint(const G4Step *step,
					const G4Track *track, bool header)
{
	if(header) {
	    printf("\n======== EventID %d TrackID %d %s ParentID %d ========\n",
	    	eventID,track->GetTrackID(),
		track->GetParticleDefinition()->GetParticleName().c_str(),
		track->GetParentID());
	    printf("Step    X(mm)    Y(mm)    Z(mm)       T(ns)      KE(MeV) Volume           Process\n");
	}
	G4ThreeVector pos = track->GetPosition();
	double time = track->GetGlobalTime();
	double ke = track->GetKineticEnergy();
	G4String vol = (step ? step->GetPreStepPoint()->GetPhysicalVolume()->
							GetName() : "");
	G4String proc = "Created";
	if(step != 0 && step->GetPostStepPoint()->GetProcessDefinedStep() != 0)
		proc = step->GetPostStepPoint()->GetProcessDefinedStep()->
				GetProcessName();
	printf("%4d %8.1f %8.1f %8.1f %11.4g %12.6f %-16s %-16s\n",
		track->GetCurrentStepNumber(),
		pos.x()/mm,pos.y()/mm,pos.z()/mm,time/ns,ke/MeV,vol.c_str(),
		proc.c_str());
}

