class BLCMDrdecaysource : public BLCommand, public BLSourceRun {
	struct VolumeData {
		const G4VPhysicalVolume *physVol;
		const G4LogicalVolume *logVol;
		const G4VSolid *solid;
		double nAtoms;
		G4AffineTransform local2global;
		VolumeData(const G4VPhysicalVolume *_physVol, double _nAtoms,
						G4AffineTransform &xform);
	};
	struct IsotopeData {
		int PDGid;
		G4ParticleDefinition *particleDefinition;
		const G4Isotope *iso;
		double totalAtoms;
		double lifetime;
		long nEvents;
		std::vector<VolumeData> volumeData;
		IsotopeData(const G4Isotope *_iso) : volumeData() {
			iso = _iso;
			PDGid = 1000000000 + iso->GetZ()*10000 + iso->GetN()*10;
			particleDefinition = G4ParticleTable::GetParticleTable()
							->FindParticle(PDGid);
			totalAtoms=0;
			lifetime=particleDefinition->GetIonLifeTime();
			nEvents=0;
		}
		void addVolume(const G4VPhysicalVolume *_physVol,
					double _nAtoms,
					G4AffineTransform &local2global);
	};
	struct DecayChain {
		G4String name;
		double ke;
		double t;
		bool isIon;
		std::vector<DecayChain*> children;
		DecayChain(const G4ParticleDefinition *pd, double _ke,
			double _t) : name(pd->GetParticleName()), ke(_ke),
			t(_t), isIon(pd->IsGeneralIon()), children() { }
		static void print(DecayChain *t=0, G4String prefix="");
		static std::vector<DecayChain*> tracks;
		static std::map<int,DecayChain*> trackMap;
		static void clear() {
			for(unsigned i=0; i<tracks.size(); ++i) delete tracks[i];
			tracks.clear();
			trackMap.clear();
		}
		static void add(const G4ParticleDefinition *pd, double _ke,
		                        double _t, int trackID, int parentID) {
		    tracks.push_back(new DecayChain(pd,_ke,_t));
		    trackMap[trackID] = tracks.back();
		    if(parentID > 0 && trackMap.count(parentID) != 0 &&
			                   	trackMap[parentID]->isIon)
			trackMap[parentID]->children.push_back(tracks.back());
		}
	};
	G4String isotopes;
	double duration;
	int nEvents;
	int first;
	double maxTime;
	int mode;
	int verbose;
	int showMarkers;
	int showDecayChain;
	int dump;
	long totalEvents;
	long generateEvents;
	long generatedEvents;
	long generatedTracks;
	std::vector<IsotopeData> isotopeData;
	std::set<G4String> isotopeNames;
	BLTrackFile *out;
	G4ParticleGun *particleGun;
	bool generating;
	int lastTrackID;
	G4String sourceIon;
	double lostKE;
	double decayTime;
	double weight;
	int eventID;
	long startTimems;
	static BLMarkers *markers; // must be persistent
public:
	BLCMDrdecaysource();

	G4String commandName() { return "rdecaysource"; }
	
	int command(BLArgumentVector& argv, BLArgumentMap& namedArgs);

	void defineNamedArgs();

	// from BLSourceRun
	virtual void prepare();
	virtual void shutDown();
	virtual void BeginOfEventAction(const G4Event* event);
	virtual void EndOfEventAction(const G4Event* event);
	virtual void PreUserTrackingAction(const G4Track *track);
	virtual void UserSteppingAction(const G4Step *step);
	virtual G4ClassificationOfNewTrack ClassifyNewTrack(const G4Track*);
	virtual bool nextSourceEvent(G4Event *event);
private:
	/// scans the physVol for all physical volumes containing a desired
	/// radioactive isotope. Volumes with multiple radioactive isotopes
	/// will be entered once in isotopeData for each isotope.
	void scan(const G4VPhysicalVolume *physVol, 
					G4AffineTransform &local2global);

	/// helper function to handle an isotope.
	void handleIsotope(const G4Isotope *iso,
				const G4VPhysicalVolume *physVol,double nAtoms,
				G4AffineTransform &local2global);

	/// returns true if the isotope is radioactive (i.e. lifetime > 0).
	bool isRadioactive(const G4Isotope *iso, G4String prefix="");

	/// generates a point in the solid. Gets the extent of the solid
	/// and generates randomly in the box, checking for inside the solid,
	/// so this works for any type of solid.
	G4ThreeVector generatePointInSolid(const G4VSolid *s);

	/// prints stepping verbose for this class (different from BLManager
	/// because time can be extremely large).
	void steppingVerbosePrint(const G4Step *step, const G4Track *track,
							bool header=false);

	/// dumps the isotope table for all isotopes listed in the NIST 
	/// database.
	void dumpTable();
};
